#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char	*t;

	t = NULL;
	while (1)
	{
		if (*s == (char)c)
			t = s;
		if (*s == '\0')
			break ;
		s++;
	}
	return ((char *)t);
}
